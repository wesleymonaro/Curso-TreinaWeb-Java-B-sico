package br.com.treinaweb.parte1;

public class OperadoresRelacionais {

  public static void main(String[] args) {
    int i = 3;
    int j = 5;
    System.out.println(i > j); // false

    System.out.println(i >= j); // false

    System.out.println(i < j); // true

    System.out.println(i <= j); // true

    System.out.println(i == j); // false

    System.out.println(i != j); // true
  }

}