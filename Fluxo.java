package br.com.treinaweb.parte2;

public class Fluxo {

  public static void main(String[] args) {
    int ano = 0;
    ano = 2012;

    if (ano == 2012){
      //Somente será executado se o ano for 2012
      System.out.println("O ano é 2012");
    }else{
      //Somente será executado se o ano não for 2012
      System.out.println("O ano não é 2012");
    }

  }

}