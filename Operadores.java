package br.com.treinaweb.parte1;

public class Operadores {

  public static void main(String[] args) {
    int i;
    //Operador binário onde i é o operando
    i = 0;
    System.out.println(i);

    //Operador unário onde i é o operando
    i++;
    System.out.println(i);

    System.out.println(i > 0 ? "Maior" : "Menor");
  }
}