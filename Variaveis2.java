package br.com.treinaweb.parte1;

public class Variaveis2 {
  public static void main(String[] args) {
    int i = 9;
    double d = 3.14;
    char c = 'W';
    boolean b = false;

    System.out.println("i = " + i);
    System.out.println("d = " + d);
    System.out.println("c = " + c);
    System.out.println("b = " + b);
  }

}