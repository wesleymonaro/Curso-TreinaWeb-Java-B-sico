package br.com.treinaweb.parte1;

public class OperadoresMatematicos {

  public static void main(String[] args) {
    int i = 0;
    System.out.println(i);//imprime 0
    System.out.println(i++);//imprime 0
    System.out.println(i);//imprime 1
    System.out.println(++i);//imprime 2
    System.out.println(i);//imprime 2
  }

}